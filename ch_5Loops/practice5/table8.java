package ch_5Loops.practice5;

public class table8 {
    public static void main(String[] args) {
        int n = 8 ;
        int sum = 0;
        for( int i = 1 ; i <=10 ; i++){
            sum = sum+ n*i ;
         
        }
        System.out.println("The sum of table 8 is :");
        System.out.println(sum);
    }
    
}
