public class operators {
    public static void main(String[] args) {

        // Arithematic operators :
        System.out.println( 2+3);
        System.out.println(6%4);
        System.out.println(4.8%1.1);

        //Assignment Operators : assigns the value 
        int b = 7 ;
        System.out.println(b+=3);

        // Comparison operator : compares the operators 
        System.out.println(67>2);
        System.out.println(87<32);

        //Logical operator : and (&&), or (||)
            // && both condition are true 
            // || one condition is either true or false  returns true 

            System.out.println(68<71 && 71<65);
            System.out.println(68<71 || 71<65);
    }
    
}
